#Tipos de datos
#String 
print (type('Esto es un texto'))

#Integer | enteros
print (type (30))

#Float | decimales
print (type(10.5))

#Booleans
print (type (False))

#List | Arrays
lista=[3,5,'test']
print (type(lista))

#tuple = elementos que no voy a cambiar
tupla=(10,55,'lola',26)
print (type(tupla))

#Diccionarios
dicc={'x':23, 'y':65, 'z': 'texto'}
print(type(dicc))